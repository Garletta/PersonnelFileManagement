package com.jx.entity;


public class Staff {

  private String staffId;
  private String staffName;
  private String staffSex;
  private Integer staffAge;
  private String staffEducation;
  private String staffPosition;
  private String staffTitle;
  private String staffAddress;
  private String staffTel;
  private String staffSalaryLevel;
  private String staffDepartment;
  private long working;

  public String getStaffId() {
    return staffId;
  }

  public void setStaffId(String staffId) {
    this.staffId = staffId;
  }

  public String getStaffName() {
    return staffName;
  }

  public void setStaffName(String staffName) {
    this.staffName = staffName;
  }

  public String getStaffSex() {
    return staffSex;
  }

  public void setStaffSex(String staffSex) {
    this.staffSex = staffSex;
  }

  public Integer getStaffAge() {
    return staffAge;
  }

  public void setStaffAge(Integer staffAge) {
    this.staffAge = staffAge;
  }

  public String getStaffEducation() {
    return staffEducation;
  }

  public void setStaffEducation(String staffEducation) {
    this.staffEducation = staffEducation;
  }

  public String getStaffPosition() {
    return staffPosition;
  }

  public void setStaffPosition(String staffPosition) {
    this.staffPosition = staffPosition;
  }

  public String getStaffTitle() {
    return staffTitle;
  }

  public void setStaffTitle(String staffTitle) {
    this.staffTitle = staffTitle;
  }

  public String getStaffAddress() {
    return staffAddress;
  }

  public void setStaffAddress(String staffAddress) {
    this.staffAddress = staffAddress;
  }

  public String getStaffTel() {
    return staffTel;
  }

  public void setStaffTel(String staffTel) {
    this.staffTel = staffTel;
  }

  public String getStaffSalaryLevel() {
    return staffSalaryLevel;
  }

  public void setStaffSalaryLevel(String staffSalaryLevel) {
    this.staffSalaryLevel = staffSalaryLevel;
  }

  public String getStaffDepartment() {
    return staffDepartment;
  }

  public void setStaffDepartment(String staffDepartment) {
    this.staffDepartment = staffDepartment;
  }

  public long getWorking() {
    return working;
  }

  public void setWorking(long working) {
    this.working = working;
  }
}
